# converting list '[]' to tuple
tple1 = [1,2,3,4,5,6,"jasi",1]
print(tuple(tple1))

#tuple having integers
tpl1 = (1,2,3)
print(tpl1)

#tuple with mixed datatypes
tpl2 = (1,"Hello",3.4)
print(tpl2)

#nested tuple
tpl3 = ("mouse",[8,4,6],(1,3,2))
print(tpl3)

#tuple can be created without parentheses 
#also called tuple packing

tpl4 = 3, 4.6,"dog"
print(tpl4)

