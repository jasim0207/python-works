#get - access the values inside the dictionaries
"""
get() method
keys()
values()
items()
"""
dict1 = {
    "brand":"ford",
    "model":"mustang",
    "year": 1964
}
x = dict1.get("brand")
y = dict1.keys() #keys method- access the keys inside the dictionary
z = dict1.values()# values() access the all values inside the dictionary
a = dict1.items() #items
print(x)
print(y)
print(z)
print(a)





