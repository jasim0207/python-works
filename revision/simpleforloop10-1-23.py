"""
simple for loop - there is no condition in single for loop
"""
for i in [10,20,30]:
    print(i)
for i in 'python':
    print(i)

for i in range(10):   #start,stop,step
    print(i)

for x in range(2,10): # start with 2 , increment till 9
    print(x)
for x in range(2,10,2): # start with 2 increment till 9, increment with 2.
    print(x)

